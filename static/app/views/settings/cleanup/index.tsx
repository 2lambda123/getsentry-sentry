const users = [];

export default function OrganizationCleanup({location}: OrganizationCleanupProps) {
  const organization = useOrganization();
  const api = useApi();
  const category: CleanupCategory = location.query.category ?? 'projects';

  const {
    data = [],
    isLoading,
    isError,
    refetch,
  } = useApiQuery<Team[] | User[] | Project[]>(
    [`/organizations/${organization.slug}/cleanup/`, {query: {category}}],
    {
      staleTime: 0,
      retry: false,
    }
  );

  const tempData =
    category === 'projects' ? projects : category === 'teams' ? teams : users;

  function handleRemoveProject(project: Project) {
    removePageFiltersStorage(organization.slug);
    removeProject({
      api,
      orgSlug: organization.slug,
      projectSlug: project.slug,
      origin: 'settings',
    })
      .then(() => {
        addSuccessMessage(t('%s was successfully removed', project.slug));
      })
      .catch(err => {
        addErrorMessage(t('Error removing %s', project.slug));
        handleXhrErrorResponse('Unable to remove project', err);
      });
  }

  const isProjectAdmin = (project: Project) =>
    hasEveryAccess(['project:admin'], {organization, project});

  function handleRemoveTeam(team: Team) {
    removeTeam(api, {orgId: organization.slug, teamId: team.slug})
      .then(() => {
        addSuccessMessage(t('%s was successfully removed', team.slug));
      })
      .catch(err => {
        addErrorMessage(t('Error removing %s', team.slug));
        handleXhrErrorResponse('Unable to remove project', err);
      });
  }

  const hasTeamAdmin = (team: Team) =>
    hasEveryAccess(['team:admin'], {organization, team});

  return (
    <Fragment>
      <SettingsPageHeader title={t('Cleanup')} />
      <p>
        {t(
          'Delete unused projects, empty teams and inactive users in your organization.'
        )}
      </p>

      <Layout>
        <TabList>
          <TabList.Item
            key="projects"
            to={{
              pathname: location.pathname,
              query: {category: 'projects'},
            }}
          >
            {t('Projects')}
          </TabList.Item>
          <TabList.Item
            key="teams"
            to={{
              pathname: location.pathname,
              query: {category: 'teams'},
            }}
          >
            {t('Teams')}
          </TabList.Item>
          <TabList.Item
            key="users"
            to={{
              pathname: location.pathname,
              query: {category: 'users'},
            }}
          >
            {t('Users')}
          </TabList.Item>
        </TabList>

        {!isError ? (
          <LoadingError onRetry={refetch} />
        ) : (
          <StyledPanelTable
            headers={[capitalize(category), '']}
            isLoading={isLoading}
            isEmpty={tempData.length === 0}
          >
            {tempData.map((object: any) => {
              if (category === 'projects') {
                const project = object as Project;
                return (
                  <Fragment key={project.id}>
                    <FlexCenter>
                      <ProjectBadge project={project} avatarSize={16} />
                    </FlexCenter>

                    <FlexCenterRight>
                      <Confirm
                        onConfirm={() => handleRemoveProject(project)}
                        priority="danger"
                        confirmText={t('Remove Project')}
                        disabled={!isProjectAdmin(project) || project.isInternal}
                        message={
                          <div>
                            <TextBlock>
                              <strong>
                                {t(
                                  'Removing this project is permanent and cannot be undone!'
                                )}
                              </strong>
                            </TextBlock>
                            <TextBlock>
                              {t('This will also remove all associated event data.')}
                            </TextBlock>
                          </div>
                        }
                      >
                        <div>
                          <Button size="sm">{t('Remove Project')}</Button>
                        </div>
                      </Confirm>
                    </FlexCenterRight>
                  </Fragment>
                );
              }
              if (category === 'teams') {
                const team = object as Team;
                return (
                  <Fragment key={team.id}>
                    <FlexCenter>
                      <Link
                        data-test-id="team-link"
                        to={`/settings/${organization.slug}/teams/${team.slug}/`}
                      >
                        <IdBadge
                          team={team}
                          avatarSize={32}
                          description={tn('%s Member', '%s Members', team.memberCount)}
                        />
                      </Link>
                    </FlexCenter>
                    <FlexCenterRight>
                      <Confirm
                        disabled={!hasTeamAdmin(team)}
                        onConfirm={() => handleRemoveTeam(team)}
                        priority="danger"
                        message={t(
                          'Are you sure you want to remove the team %s?',
                          `#${team.slug}`
                        )}
                      >
                        <Button size="sm">{t('Remove Team')}</Button>
                      </Confirm>
                    </FlexCenterRight>
                  </Fragment>
                );
              }
              if (category === 'users') {
                const user = object as User;
                return (
                  <Fragment key={user.id}>
                    <UserBadge user={user} avatarSize={16} />
                  </Fragment>
                );
              }

              return null;
            })}
          </StyledPanelTable>
        )}
      </Layout>
    </Fragment>
  );
}

const Layout = styled('div')`
  display: flex;
  flex-direction: column;
  gap: ${space(2)};
`;

const StyledPanelTable = styled(PanelTable)``;

const FlexCenter = styled('div')`
  ${p => p.theme.overflowEllipsis}
  display: flex;
  align-items: center;
  line-height: 1.6;
`;

const FlexCenterRight = styled(FlexCenter)`
  justify-content: flex-end;
`;
